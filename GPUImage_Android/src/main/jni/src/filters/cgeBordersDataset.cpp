/*
By Wang Yang.

简要介绍：
ParsingEngine的目的是让完全不懂GLSL的人也知道如何快速添加新的特效。

BorderString解析规则和常用方法说明：

1. 每一步不同的处理均以'@'开头，后面跟处理方式。中间可以有空格或者没有
例: "@ method" 或者 "@method" 均为正确格式。method后面可以跟任意个字符，以空格隔开。
method后面的所有字符(直到下一个"@"符号或者字符串结束)将作为参数传递给该method对应的Parser.

2. coverS方法参数解释： @coverS方法格式为 "@coverS <texture>"

<texture> 表示用到的边框资源文件名。（包含后缀，当前文档描述的所有<texture>参数含义相同！）
注: 本方法对应旧的边框方法 S.

3. coverM方法参数解释： @coverM方法格式为 "@coverM <texture> <width> <height> <block_leftTop> <block_top> <block_rightTop> <block_right> <block_rightBottom> <block_bottom> <block_leftBottom> <block_left>"
参数解释：
<texture> 参见第二条。
<width> <height> 分别表示该资源图片的(逻辑/真实)宽高。
<block_*> 表示coverM时所需要占据的图片的八个位置，由于并没有前置标记，请保持该顺序。
每一个<block_*> 参数包含四个数，分别表示该block在资源图片中的 left, top, width, height。

注: 如果使用逻辑宽高，<width> <height> 可写为1， <block_*>参数使用该block在原图中的相对位置[0, 1)。 coverM 方法对应旧的边框方法 M.

4. coverskr方法参数解释： @coverskr方法格式为 "@coverskr <texture> <width> <height>"
参数解释：所有参数与上面描述对应相同。
注： 本方法与coverS效果类似，但会保持边框资源的比例进行缩放并##居中覆盖全图##。

5. coverfullkr方法参数解释： @coverfullkr方法格式为 "@coverfullkr <texture>"
注： 本方法与coverS效果类似， 但会保持边框资源的比例进行缩放，并##居中整个放置于原图之内##。

6. covercorners方法参数解释： @covercorners方法格式为 "@covercorners <texture> <width> <height> <block_leftTop> <block_rightTop> <block_rightBottom> <block_leftBottom>"
注： 本方法参数与coverM含义类似。 区别在于，本方法仅包含四个 <block_*> 参数，分别表示四个corner。

7. coverudt方法参数解释： @coverudt方法格式为 "@coverudt <texture> <width> <height> <block_up> <block_down> <block_tex> <tex_align>"

<tex_align> 参数包含一个描述字符串和2~4个整数。描述字符串包括：
"CB"(中间靠下方), "LB"(左侧靠下方), "RB"(右侧靠下方)
"CT"(中间靠上方), "LT"(左侧靠上方), "RT"(右侧靠上方)
"XC"(水平方向自定义，垂直方向中间对齐) "CX"(水平方向中间对齐，垂直方向自定义）
"XX"(水平垂直均自定义)。
其中如果参数中包含一个'X',则下一个参数为该位置上的自定义值，如果包含两个'x'，则后两个参数均为自定义值.
再后面两个整数分别表示需要放置的tex在原图中占据的大小，取值范围相对于原图大小[0,1].

注： 本方法参数与coverM含义类似。区别在于新增 <tex_align> 参数。效果为循环使用<block_*>分别覆盖原图的上下方。

8. coverstex方法参数解释： @coverst方法格式为 "@coverstext <texture> <width> <height> <block_full> <block_tex> <tex_align> <border_size>"
参数解释：
<block_*> 格式与前面相同， <text_area> 与方法7 相同。
效果类似于coverS，增加了block_tex与tex_align. <tex_align>含义与上面相同。
<border_size> 参数为可选参数，如果不填则默认为0. 此参数将确定tex_align所取的边界 

9. coverstkr 方法参数解释： @coverstkr方法格式为 "@coverstkr <texture> <width> <height> <block_full> <block_tex> <tex_align> <border_size>"
参数解释：
所有参数含义、格式与coverstext 相同，效果上，将保持资源图<block_full>的比例进行等比缩放覆盖至原图。

10. cover2ct 方法参数解释： @cover2ct 方法格式为 "@cover2ct <texture> <width> <height> <block_right_top> <block_left_bottom> <block_tex> <tex_align>"
参数解释：
<block_*>参数格式与前面相同， <tex_align> 参数格式与前面相同。
效果： 在右上角与左上角以及tex_align指定的位置添加三个装饰图。

11. covermtex 方法参数解释： @covermtext 方法格式为:  "@covermtext <texture> <width> <height> <block_leftTop> <block_top> <block_rightTop> <block_right> <block_rightBottom> <block_bottom> <block_leftBottom> <block_left> <block_tex> <tex_align> <border_size>"
参数解释：
除去最后两个参数，其余所有参数含义与格式和@coverM相同。<block_tex> <tex_align> 含义与方法7相同。
<border_size> 包含两个参数，分别为borderSizeX和borderSizeY
它是可选参数，当不填时，默认为0.1
效果: 在coverM效果之上增加 <block_tex> <tex_align> <border_size>

12. coverbkr 方法参数解释： @coverbkr 方法格式为： "@coverbkr <texture> <width> <height>"
效果： 循环使用<block_bottom>覆盖原图的下方，并将<block_text>放置于<text_area>指定的位置中。

13. card 方法参数解释： @card 方法格式为: "@card <texture> <picture_area> <align>"
<texture> 为需要使用到的图片名。
<picture_area> 为卡片上显示图片的区域，包含四个参数，分别表示该区域的左侧，上侧，宽度，高度。范围[0, 1]
<align> 为图片等比缩放之后的对齐方式，范围为 [0, 2]， 1 表示居中对齐。

///////////////  以上边框效果 均依赖SDK版本 0.2.1.1 及其以上///////////////////

14. udcover 方法参数解释： @udcover 方法格式为: "@udcover <texture> <width> <height> [thickness]"
前面三个参数与前面提到的同名参数相同。
[thickness] 为可选参数，若不填则默认为 0.1

15. lrcover 方法参数解释: @lrcover 方法格式为: "@lrcover <texture> <width> <height> [thickness]"
所有参数与 updowncover 相同。

///////////////  以上边框效果 均依赖SDK版本 0.3.1.1 及其以上///////////////////

*/

